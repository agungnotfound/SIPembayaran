    private void tblMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuMouseClicked
        int row = tblMenu.getSelectedRow();
        String id = tblMenu.getValueAt(row, 0).toString();
        menu = menuCtrl.findMenu(id);
        if (menu != null) {
            txtId.setText(menu.getIdMenu());
            txtNama.setText(menu.getNama());
            txtHarga.setText(Double.toString(menu.getHarga()));
            cmbStatus.setSelectedItem(menu.getStatus());
        }
    }//GEN-LAST:event_tblMenuMouseClicked


