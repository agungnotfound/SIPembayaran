    private boolean isDataValid(){
        if (txtNama.getText().equals("")) {
            return false;
        } else if(txtHarga.getText().equals("")){
            return false;
        }else{
            return true;
        }
    }
    
    public void refresh(){
        tblMenu.updateUI();
        menuList.clear();
        menuList.addAll(menuCtrl.findMenuEntities());
    }
    
    public void reset(){
        txtNama.setText("");
        txtHarga.setText("");
        cmbStatus.setSelectedItem(0);
        menu = null;
    }

