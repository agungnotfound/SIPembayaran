    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (!isDataValid()) {
            JOptionPane.showMessageDialog(null, "Isi Semua Data!!");
        } else {
            boolean isNew = menu == null;
            if (isNew) {
                menu = new Menu();
            }
            
            menu.setIdMenu(txtId.getText());
            menu.setNama(txtNama.getText());
            menu.setHarga(Double.parseDouble(txtHarga.getText()));
            menu.setStatus(cmbStatus.getSelectedItem().toString());
            
            
            if (isNew) {
                try {
                    menuCtrl.create(menu);
                    JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
                    reset();
                    refresh();
                    tblMenu.updateUI();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Mohon periksa data kembali!");
                    Logger.getLogger(FrmMenu.class.getName()).log(Level.SEVERE, null, e);
                }
                
            } else {
                try{
                    menuCtrl.edit(menu);
                    JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
                    refresh();
                    tblMenu.updateUI();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Data Gagal di Ubah");
                    Logger.getLogger(FrmMenu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

